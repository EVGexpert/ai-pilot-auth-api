/**
 * Mode Router — патчи системного промпта по authoring mode сайта.
 *
 * Режим определяется из capability profile (GET /agent/capabilities на сайте).
 * Если профиль отсутствует или режим неизвестен — getModePromptSnippet
 * возвращает '' (generic prompt, обратная совместимость).
 *
 * Структура расширяема: новые режимы добавляются как ключи MODE_PROMPTS.
 * AP-009 (YAGNI): реализован только режим 'authoring' + fallback.
 */

export const MODE_PROMPTS = {
  authoring: `
Режим сайта: AUTHORING.
- Клиент может редактировать контент: посты, страницы, рубрики, опции.
- Предлагай конкретные действия через action-блок (create_post, update_post и т.д.).
- Перед опасными изменениями (удаление, публикация) — подтверждай с клиентом.
- Учитывай существующую структуру сайта: не дублируй посты, проверяй slug.
`,
  chat_only: `
Режим сайта: CHAT ONLY (read-only).
- Отвечай на вопросы о сайте, консультируй, объясняй.
- НЕ предлагай действия (create_post, update_post и т.д.) — действия недоступны.
- Если клиент просит изменить сайт — объясни, что нужно переключить режим.
`
}

/**
 * Вернуть дополнительный блок правил для режима.
 * unknown/отсутствующий режим → '' (generic prompt как раньше).
 */
export function getModePromptSnippet(mode) {
  if (!mode || typeof mode !== 'string') return ''
  return MODE_PROMPTS[mode] || ''
}
